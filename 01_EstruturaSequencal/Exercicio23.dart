// Elaborar um programa que calcule e armazene uma raiz de base qualquer com índice qualquer.


void main() {
  // Solicitar a leitura da base e do índice
  print('Digite a base da raiz:');
  double baseRaiz = 16;

  print('Digite o índice da raiz:');
  double indiceRaiz = 4;

  // Apresentar o resultado diretamente
  print('A raiz de índice $indiceRaiz e base $baseRaiz é: ${(baseRaiz, 1 / indiceRaiz)}');
}
